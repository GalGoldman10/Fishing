export function findFishGuideEntry(query: string): MediterraneanFishGuideEntry | undefined {
  const q = query.trim().toLowerCase();
  if (!q) return undefined;
  return MEDITERRANEAN_FISH_GUIDE.find((entry) => {
    if (entry.hebrewName.includes(query.trim())) return true;
    if (entry.englishName.toLowerCase().includes(q)) return true;
    return entry.aliases.some((alias) => alias.toLowerCase().includes(q) || alias.includes(query.trim()));
  });
}
