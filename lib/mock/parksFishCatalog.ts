export function findParksFishByText(query: string): ParksFishSpecies | undefined {
  const q = query.trim();
  if (!q) return undefined;
  const lower = q.toLowerCase();
  return PARKS_FISH_CATALOG.find((entry) => {
    if (entry.officialNameHe.includes(q)) return true;
    if (entry.scientificName.toLowerCase().includes(lower)) return true;
    return entry.colloquialNames.some((name) => name.includes(q) || name.toLowerCase().includes(lower));
  });
}
